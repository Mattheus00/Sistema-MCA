def norm_nome(s: str) -> str:
    s = unicodedata.normalize("NFKD", s)
    s = "".join(c for c in s if not unicodedata.combining(c))
    s = s.upper()
    s = re.sub(r"\s+", " ", s).strip()
    return s


def only_digits(s: str) -> str:
    return re.sub(r"\D", "", s)


def parse_relatorio() -> list[tuple[str, str]]:
    rows = []
    for line in RELATORIO.splitlines():
        line = line.strip()
        if not line:
            continue
        if "\t" in line:
            nome, doc = line.split("\t", 1)
        else:
            parts = line.rsplit(None, 1)
            if len(parts) != 2:
                continue
            nome, doc = parts
        doc = only_digits(doc)
        if len(doc) not in (11, 14):
            print(f"AVISO: documento inválido para {nome}: {doc}")
        rows.append((norm_nome(nome), doc))
    return rows


def main():
    relatorio = parse_relatorio()
    por_nome: dict[str, list[str]] = defaultdict(list)
    for nome, doc in relatorio:
        por_nome[nome].append(doc)

    with CSV_IN.open(encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = list(reader.fieldnames or [])
        if "cpf_cnpj" not in fieldnames:
            fieldnames.append("cpf_cnpj")
        rows_out = []
        sem_match = []
        for row in reader:
            nome_csv = row["nome"]
            chave = norm_nome(nome_csv)
            fila = por_nome.get(chave)
            if fila:
                row["cpf_cnpj"] = fila.pop(0)
            else:
                # tentativa: nome parcial sem sufixos ME/LTDA
                row["cpf_cnpj"] = ""
                sem_match.append((row.get("codigo"), nome_csv, chave))
            rows_out.append(row)

    with CSV_OUT.open("w", encoding="utf-8", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        w.writerows(rows_out)

    matched = sum(1 for r in rows_out if r.get("cpf_cnpj"))
    print(f"CSV atualizado: {matched}/{len(rows_out)} com CPF/CNPJ")
    if sem_match:
        print(f"Sem match ({len(sem_match)}):")
        for cod, nome, chave in sem_match:
            print(f"  [{cod}] {nome} ({chave})")

    sobrando = {k: v for k, v in por_nome.items() if v}
    if sobrando:
        print(f"Relatório não usado ({sum(len(v) for v in sobrando.values())} docs):")
        for k, v in sobrando.items():
            print(f"  {k}: {v}")


if __name__ == "__main__":
    main()
